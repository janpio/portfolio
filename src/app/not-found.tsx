/**
 * This is a functional component that renders a "Not Found" message with a description of the requested resource not
 * being found.
 *
 * @returns {JSX.Element} The "Not Found" message.
 */
export default function NotFound() {
  return (
    <div>
      <h2>Not Found</h2>
      <p>Could not find requested resource</p>
    </div>
  )
}
